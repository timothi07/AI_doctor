import webbrowser
import pickle
import streamlit as st 
from streamlit_option_menu import option_menu
from drugdata import get_drug_details

#loading saved models

diabetes_model = pickle.load(open('/home/timothi/Desktop/disease predection system/Saved files/diabetes_model.sav','rb'))

heart_disease_model = pickle.load(open('/home/timothi/Desktop/disease predection system/Saved files/heart_disease_model.sav','rb'))

parkinsons_model = pickle.load(open('/home/timothi/Desktop/disease predection system/Saved files/parkinsons_model.sav','rb'))

# sidebar for navigation

with st.sidebar:
    
    selected = option_menu('Disease Prediction System',
                           ['Diabetes Prediction',
                            'Heart Disease Prediction',
                            'Parkinsons Prediction'],
                           default_index = 0)
    
    #button
    url="http://172.16.55.209:5000/"
    if st.button('Upload X-Ray'):
        webbrowser.open_new_tab(url)
        
    st.text('Get Medicine Details')
    drug = st.text_input('Enter Medicine')
